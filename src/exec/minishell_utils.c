#include "../../includes/minishell.h"

int count_args(char *input)
{
    int count;
    int in_word;

    count = 0;
    in_word = 0;
    while (*input)
    {
        if (*input == ' ' || *input == '\t')
            in_word = 0;
        else if (!in_word)
        {
            count++;
            in_word = 1;
        }
        input++;
    }
    return count;
}

void free_args(char **args)
{
    int i;

    i = 0;
    while (args[i])
    {
        free(args[i]);
        i++;
    }
    free(args);
}